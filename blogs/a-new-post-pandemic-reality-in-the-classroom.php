                     <br><br>

                     <p style="color: black;">At the height of the COVID-19 crisis, an estimated 1.2 billion children were affected by school closures around the globe.  Educators were forced to rethink their methods, seek out alternative resources, and leverage new tools. As teaching and learning moved online, many discovered the utility of augmented reality for the first time. </p>

                     <h2>Alpha Students</h2>

                     <p style="color: black;">
                     Members of the Alpha Generation—young people born after 2010—are uniquely positioned to not only take digital transformation in stride but to gain from it. Sometimes known as the iGeneration, it is the most technology-literate group in history. Born in an era when smartphones, tablets, and computers were already ubiquitous, they have never known a time without video games or the internet, and by 2025 there will be two billion of them, according to statisticians.
                     </p>  
                     
                     <p style="color: black;">
                     While there’s no question that students reap myriad social and emotional benefits from in-person learning, as teachers introduced AR technology into the remote classroom, Generation Alpha students proved, for the most part, to be skilled and enthusiastic users. Sure--some of that enthusiasm might have been triggered by anything capable of breaking the monotony of the COVID-19 lockdowns, but education researchers have discovered there’s a lot more to the story than that. 
                     </p>

                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/pandemicClassroom.png" alt="Image" class="rounded"> 

                     <br><br>

                     <h2>Going Beyond Engagement</h2>            

                     <p style="color: black;">
                     Besides the obvious ability to banish boredom, AR applications allow teachers to:
                     </p>    

                     <ul style="color: black;">
                        <li>Create a rich learning environment, with multisensory, hands-on elements that enable students to absorb concepts more quickly </li>
                        <li>Increase the focus of students diagnosed with attention deficit disorders, who can block outside distractions by wearing AR/VR headsets </li>
                        <li>Boost their students’ sensory development by incorporating visual, auditory, and in some cases, even haptic learning cues</li>
                        <li>Enhance collaboration and cooperation among peers in a way that hearing a lecture or filling out worksheets cannot match </li>
                        <li>Assign enriching AR-enhanced textbooks that enable students to supplement their reading with video reinforcement and interactive graphics</li>
                        <li>Greatly increase the retention of material: researchers have found that the average student can remember only 30% of what they hear from a teacher standing at the front of a classroom but a full 90% of what they’ve learned through experience</li>
                    </ul>

                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/pandemicPhone.png" alt="Image" class="rounded"> 

                     <br><br>

                     <h2>There’s an App for That</h2>

                     <p style="color: black;">
                     No matter the subject, from chemistry to history to literature, there’s an AR app that will apply. You don’t have to travel to the Jurassic Park exhibit at Universal Studios in Orlando, to get up close and personal with a velociraptor, for example; any day can feel like the Cretaceous Period given the proper technology. Want to convey the devastation of the world’s coral reefs in a visceral, actionable way? There’s an app that allows students to walk a virtual ocean floor and see for themselves. And if students are finding their study of the Gallic Wars dry and unengaging, a virtual field trip to Caesar’s battlefields might solve the problem. 
                     </p>

                     <h2>Mission Possible</h2>

                     <p style="color: black;">
                     Especially at the start of the pandemic, there was fear that some courses of study would simply be impossible to undertake on-line. How could a nursing student, for example, learn to place a catheter, dress wounds, or start an IV line without ever working on an actual patient. Developers of AR platforms leapt to the rescue, and used in conjunction with mannequins or camera footage of patients, the apps they created allowed aspiring healthcare providers to gain proficiency safely and at their own pace. 
                     </p>

                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/pandemicFamily.png" alt="Image" class="rounded"> 

                     <br><br>

                     <h2>It All Adds Up</h2>

                     <p style="color: black;">
                     Within the next two years, industry experts predict that almost 2.5 billion smartphone users will have at least one AR app downloaded and that the AR market will exceed $70 billion, with even greater potential as prices for VR/AR equipment drop and adoption becomes more widespread.  
                     </p>

                     <p style="color: black;">
                     Although developers must keep usability issues in mind, and educators should take care to integrate the technology into their lesson plans thoughtfully, it’s clear that AR, done right, brings new perspectives and possibilities to the education sector. Its quantifiable benefits mean that AR in the classroom is here to stay—particularly as Generation Alpha prepares for their future. 
                     </p>


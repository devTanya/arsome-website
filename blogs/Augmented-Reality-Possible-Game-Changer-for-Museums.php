                     <p class="lead" style="color: black;"> As museums open, they may be forced to become a little more creative with their options. The dramatic change in the museum industry’s economy is an opportunity to do more. </p>
            <img src="../images/blog/0_J3br5Cc4TbuPjXfs.jpeg" alt="Image" class="rounded"> <br>
 <br>
                     <p style="color: black;">Museums have begun announcing reopening dates, from larger ones like NYC’s Metropolitan Museum of Art to smaller ones like Connecticut’s Wadsworth Atheneum. Covid-19 has not only stalled the profits gained by museums, but has also forced some museums to close down. In fact, “a June survey from the America Alliance of Museums found that as many as 12,000 of America’s museums may close for good.” This is a startling statistic that brings into question how museums will adapt and be able to continue making a profit even when their doors are closed or they are being forced to serve at a lower capacity than normal. It also begs the question, what will museums do if they’re forced into a similar situation in the future — especially with another possible spike in Covid cases as we enter colder months. Augmented reality should be an avenue that museums look into as they make changes to their operations.</p>

                     <p style="color: black;">Augmented reality provides a variety of options for museums. It allows for distance-based learning where visitors can bring pieces of the exhibit home and even interact with them. Imagine looking at a painting that had been refurbished, then doing a mini refurbishing project at home through AR with the museum’s app.</p>

                     <p style="color: black;">Additionally, by adding AR to an already existing app or even creating an app with AR functionality, you open a world of possibilities through that app. With an app, you can communicate better with loyal customers, offer discounts, provide surveys, and offer a digital ticketing system which would decrease the possibility of cross contamination between employees and visitors. The creation of an app can make a world of difference for your museum, as long as it’s done right and with the viewer in mind.</p>


                     <img src="../images/blog/0_15ny9qzk5xh6gg-l.jpeg" alt="Image" class="rounded"><br>
                   <br>

                     <p style="color: black;">The AR in the app would give people a reason to visit the museum. You could set up scavenger hunts in the physical museum that is dictated by the app. You can allow visitors to start a mini digital AR collection of their favorite pieces from the museum just through pointing at the actual pieces in the museum.</p>

                     <p style="color: black;">AR also allows for people to stand back while still being able to read information on the art piece. For people hard of seeing, sometimes it can be hard to read the descriptions of paintings and sculptures or their creators without getting very close, which often isn’t even an option. AR takes the struggle out of these moments and even can allow for clicking on parts of the pieces to zoom into with facts about those parts.</p>

                     <p style="color: black;">The museum’s AR app can also be used with audio tours. Many museums integrate audio tours into their ticketing system because it creates more of a storytelling experience. With the addition of “pull your phone out to see what this painting looked like 50 years ago” or other interesting statements like these, the storytelling experience becomes more complex and truly includes the visitor in the experience.</p>


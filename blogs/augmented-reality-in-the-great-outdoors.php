                  <p style="color: black;">In the summer of 2016, it might have seemed as though you couldn’t walk down a city street or relax in a public park without seeing someone intently staring at their mobile device and inexplicably lunging, jumping, and making audible sounds of disappointment or triumph.
</p>


<p style="color: black;">No matter where you were, from Connecticut to California and everywhere in between, the augmented reality (AR) game Pokémon GO—which encouraged users to head outdoors to find, catch, and train virtual creatures—had become a viral sensation.</p>

<img src="../images/blog/pokemon.jpeg" alt="Image" class="rounded"> 

                     <p>Don’t get me wrong, I love New York. But one of the most painful memories I have of living there is traveling to work each day. Since the entrance to the parkway only accommodated one car at a time, there was always a queue of irritated drivers waiting their turn. Once on the two-lane parkway, things didn’t get much better. Drivers were subjected to stop-and-go traffic for miles. Tempers flared as drivers leaned on their horns and cut each other off in the futile hope of making it to work on time. Ah, such fond memories!</p>
                     <p>
                        But I didn’t write this to relieve this hideous experience. It’s to make a point about 5G. We can think of the congested parkway that barely accommodates its traffic as 4G. To be fair, 4G does a creditable job as long as it’s not expected to manage too much data (like rush hour traffic).
                     </p>
                     
                     <h3>What Is 5G</h3>
                     <p>According to Kyla Ismail in <a href="https://www.cmswire.com/information-management/what-is-5g-and-how-will-it-work/" target="_blank" style="color: #2f94f9;">CMS Wire</a> , 5G wireless broadband represents the 5th generation of mobile cellular networks and promises speeds ten to twenty times faster than today’s 4G cellular networks. Ismail cited Tim Sherwood, VP of Mobile Strategy at Tata Communications, who said that the 5th generation wireless networks will provide greater speed, lower latency, and the ability to connect several devices at once.</p>

                     <blockquote class="bg-white">
                        <div class="h3 mb-2">5G allows more devices to do more and, overall, better work.</div>
                     </blockquote>

                     <h3>What Does 5G Have To Do With AR/VR</h3>
                     <p>The higher throughput of 5G enables devices to handle huge amounts of data from things as mundane as a refrigerator and as uncommon as self-driving cars — in other words 5G allows more devices to do more, and overall better, work.</p>

                     <p>
                        David Roe from <a href="https://www.cmswire.com/digital-workplace/how-5g-will-impact-augmented-and-virtual-reality-use/" style="color: #2f94f9;" target="_blank">CMS Wire</a>, cites research from Mangala Bhattacharjee, senior marketing manager at Research on Global Markets, which indicates that the initial implementation of AR applications at the consumer level (navigation, virtual tourism, immersive gaming) and enterprise level (digital twins for predictive maintenance in the manufacturing sector, remote surgeries for healthcare) will become seamless once 5G networks become widespread.
                     </p>

                     <figure>
                        <img src="../images/blog/vr-painting.jpg" alt="Image" class="rounded"> 
                        <figcaption>Photo by <a href="https://unsplash.com/@billetto?utm_source=medium&utm_medium=referral" target="_blank" class="text-muted" style="color: #000; text-decoration: underline;">Billetto Editorial</a> on <a href="https://www.unsplash.com" class="text-muted" target="_blank" style="color: #000; text-decoration: underline;">Unsplash</a></figcaption>
                     </figure>

                     <h3>
                        Current Status Of 5G In The United States
                     </h3>

                     <p>According to Thomas Duesterberg of <a href="https://www.forbes.com/sites/thomasduesterberg/2019/04/30/problems-and-prospects-for-5g-deployment-in-the-united-states/#320d203d312e" target="_blank" style="color: #2f94f9;">Forbes</a>, Huawei, the Chinese multinational technology company, has the advantage of being able to outcompete and move ahead of American companies such as Nokia, Ericsson and Samsung. Reasons for this frontrunner status in the 5G race include: the company being subsidized by China, the superior scale of its business model and the fact that it has had a head start in Europe and Asia.</p>


                     <blockquote class="bg-white">
                        <div class="h3 mb-2">By 2020, 46% of retailers planned to deploy either AR or VR solutions to meet customer service experience requirements.</div>
                     </blockquote>

                     <p>Duesterberg stated that some of the reasons 5G has not gained a foothold in this country include the capital costs of installing the needed 5G infrastructure and software upgrades and security issues related to Huawei’s 5G system.</p>

                     <figure>
                        <img src="../images/blog/motherboard.jpg" alt="Image" class="rounded"> 

                     </figure>

                     <p>
                        ack here in the US, <a href="https://www.lifewire.com/5g-availability-us-4155914" style="color: #2f94f9;" target="_blank">Lifewire</a> indicates that big carriers such as Verizon, AT&T and T-Mobile have 5G service in designated areas. For instance, Verizon offers 5G in areas of Houston Texas, Sacramento, California, Indianapolis, Indiana, Los Angeles, California and Chicago, Illinois.

                       </p>

                       <p>

                        From the retailer and consumer’s points of view, it would seem that 5G couldn’t arrive soon enough. <a href="https://www.gartner.com/en/newsroom/press-releases/2019-04-01-gartner-says-100-million-consumers-will-shop-in-augmeted" style="color: #2f94f9;" target="_blank">Gartner</a>, a research and advisory company, estimates that by 2020, 46% of retailers planned to deploy either AR or VR solutions to meet customer service experience requirements. With VR’s immersive interfaces, retailers can create task efficiencies or reduce the costs associated with designing new products. They can also enhance the understanding of information through advanced graphical visualization and simulation technologies.
                        </p>

                        <p>
                        Pilots and implementation examples include Alibaba’s full VR shopping experience, virtual reality tours by Tesco, Adidas’ VR video to promote its outdoor clothing collection, and eBay’s Australia’s partnerships with Myer to create personalized stores.
                     </p>


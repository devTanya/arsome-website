                     <h3>What Is Data Visualization</h3>
                     
                     <p style="color: black;">Remember carefully plotting out points on a piece of graph paper in middle school? You were making it easier to interpret your data by visualizing it. Data visualization, as the name implies, is the practice of placing information in a visual context—a graph, a map, or a table, for example—in order to gain insight from it. The human eye is naturally down to colors and shapes, so it’s easier for us to see patterns and outliers when information is presented visually. Sure, some people are adept at parsing the small cells of an Excel sheet and noting trends, but most of us do much better when presented with color-coded bars, bubbles, and other visual cues.</p>                 

                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/data-graphs-sample.png" alt="Image" class="rounded"> 

                     <br><br>

                     <h3>Does Size Matter?</h3>
                     
                     <p style="color: black;">Data visualization has become increasingly important in an era of Big Data—datasets so large and complex that they are difficult to analyze without sophisticated techniques. Experts estimate that 7.5 septillion (7,700,000,000,000,000,000,000) gigabytes of data are generated each day around the world, and visualizing and analyzing it efficiently can be transformative. That applies no matter what field you’re in, whether you’re a Major League Baseball official who needs to know which of his players has been most valuable during the season, a hedge fund manager hoping to predict the next day’s market, or a small businessman wanting to better understand customer behavior. In a nutshell, good data visualizations can help almost anyone make solid data-supported decisions without being a statistician.</p>
                  
                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/vr-data-visualization.png" alt="Image" class="rounded"> 

                     <br><br>

                     <h3>Is AR/VR the Next Frontier in Data Visualization?</h3>
                  
                     <p style="color: black;">Within the last decade, numerous developers began offering 3D data visualization tools, many of them free. But while a 3D chart can be colorful and compelling, what if there were a way to actually step into the picture to explore, see data points from every angle, and even manipulate them manually? Engineers can now give datapoints a direction, velocity, and mass, making even the largest datasets come alive and enabling viewers to gain multi-dimensional understanding in real time. Data scientists are now predicting that AR/VR technologies could have a major impact on how they work and the insights they can glean from datasets in every field—from medicine to finance to government.</p>
                     
                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/ar-table.png" alt="Image" class="rounded"> 

                     <br><br>
                     <p style="color: black;">Some organizations are already on it: NASA has revealed that a team at Goddard Space Flight Center has developed a VR platform that visualizes data on fluid flow and various topographic features, and researchers at Columbia Medical School recently used a platform that combines machine learning, big data, and VR to help them understand if there was a connection between cancer mortality and flu epidemics. (Their preliminary conclusion: there is.) Another company employed geospatial and mine-planning data to create a true-to-scale VR model of one of the largest undeveloped precious-metals mines in the world, allowing its owners to make a granular study of it without a large investment of time or money. Yet another is embedding geospatial data in immersive 3D maps that can then be projected onto a globe, allowing viewers to rely on familiar visual touchstones to gain a handle on analytics that might otherwise be difficult to grasp.</p>

                     <img src="https://arsome.nyc3.cdn.digitaloceanspaces.com/ar-vr-data-visualization.png" alt="Image" class="rounded"> 

                     <br><br> 
                  
                     <h3>Where Art Intersects</h3>

                     <p style="color: black;">While data visualization has always aimed to present complex information with “clarity, precision and efficiency,” as Edward Tufte, one of the pioneers of the field, has asserted, many stakeholders now think creativity should be added to that list. The possibilities for illuminating visual metaphors are endless, they say: representing data as ocean waves that change height and color as new information is input or depicting galaxies in which datapoint stars cluster in meaningful ways, for example. </p>
                     <p style="color: black;">Haptic feedback gloves are already making it possible to “feel” data as you explore it virtually—so it certainly seems like the sky (or the ocean) is the limit.  </p>
                  

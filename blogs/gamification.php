            <p class="lead">
                Gamification isn’t a new term, but few truly understand how to embrace and deploy it as a strategic tool.
              </p>
              <br>
            <center><img src="https://images.unsplash.com/photo-1554474051-0256b98c36f8?ixlib=rb-1.2.1&ixid=eyJhcHBfaWQiOjEyMDd9&auto=format&fit=crop&w=500&q=60" alt="Image" class="rounded"></center>

            <article class="article">
<br><br>
              <p>
               When I was nine-years-old my father took me to the candy store to buy a bag of marbles. Arriving home, wide-eyed with delight and anticipation, I’d empty the green, blue, gray, red and marbled (pun intended) spheres onto the living room floor. After school, my friends and I assembled at a grass-deprived lot at the end of our street and created a three-foot in diameter circle in the soil. The next order of business was setting ground rules: Were we playing for “keepies,” (players got to keep the marbles that they shot out of the circle) or just for; and who went first, second and so on.
              <p>
                At the end of each game we’d determine the winner based on who won the most marbles. When we excelled at shooting and winning, and marbles were easily knocked from the ring, we doubled the size of the circle, thereby increasing the level of difficulty. A simple game indeed. But one that had us convening to do the same thing for upteen hours on end. Why? In a few words: It was fun. It was highly motivating. But what was so motivating about it?
              </p>


              <p>
                In discussing the ingredients of successful gamification (i.e. the use of game elements and game design techniques in non-game contexts) Werbach lists three things:
              </p>

              <p><strong>1. Challenge</strong> — When playing marbles, the task of knocking opponents’ marbles out of the circle required eye-hand coordination and patience with many “misses;”</p>

              <p><strong>2. Social Component</strong> — As we played we’d often talk about the Yankees or Knickerbockers or a girl in our third grade class;</p>
              <p><strong>3. Progression</strong> - When a three-foot circle became too easy, we’d enlarge it, thereby increasing the level of difficulty of the game.</p>

 <figure>
                <img src="../images/blog/marbles.jpg" alt="Image" class="rounded">
                  <figcaption>Photo by <a href="https://unsplash.com/@crissyjarvis?utm_source=medium&utm_medium=referral" target="_blank" class="text-muted" style="color: #000; text-decoration: underline;">Crissy Jarvis</a> on <a href="https://www.unsplash.com" class="text-muted" target="_blank" style="color: #000; text-decoration: underline;">Unsplash</a></figcaption>
              
              </figure>





              <p>
             But even more than the fact that marbles had what it takes to be fun, our after-school pastime had something even more important: It was “intrinsically motivating” — a term used by Werbach. That is, it provided players with: Competence — as players improved they got a bigger stash of marbles to take home; Autonomy — players could choose to play or not, and choose which marbles they elected to use; and Relatedness — players could become better friends. Or not. Some of us were sore losers.
              </p>

              <blockquote class="bg-white">
    <div class="h3 mb-2">The overarching goal of gamification is to motivate employees to either produce more, sell more and/or pay more attention to their health. That employees need inspiration is an understatement. According to Werbach and Nevogt, nearly 70% of American employees are disengaged from their jobs. How about some examples of successful gamification efforts?</div>
  </blockquote>



              <p>
          <a href="https://www2.deloitte.com/us/en/insights/deloitte-review/issue-11/the-engagement-economy-how-gamification-is-reshaping-businesses.html" style="color: #000; text-decoration: underline" target="_blank">Palmer</a> described a customer engagement platform created to help deliver energy efficient programs. The company, O Power, had an app that allowed friends to discuss and compare their household electricity use. Users left tips, provided support, and shared successes as they challenged each other to reduce their energy usage. This resulted in hundreds of millions of kilowatt hours saved as real-world data combined with an ability to socialize and receive rewards, changed behavior.
              </p>

 <figure>
                <img src="../images/blog/hands.jpg" alt="Image" class="rounded">

</a> on <a href="https://www.unsplash.com" class="text-muted" target="_blank" style="color: #000; text-decoration: underline;">Unsplash</a></figcaption>
              
              </figure>

              <p>
                <a href="https://elearningindustry.com/3-mistakes-youre-making-gamification-business" style="color: #000; text-decoration: underline" target="_blank">Wroten</a> chronicled an insurance company in Buffalo, NY, that designed a sales competition whereby employees earned points and competed to win tickets to a Buffalo Sabres game.
              </p>

              <blockquote class="bg-white">
    <div class="h3 mb-2">Cito Research indicated that in two weeks the insurance company had 152% more updates to their sales opportunity data than in the previous eight months combined.</div>
  </blockquote>

  <p>
    Werbach recounted a company that, in an effort to improve the health of its employees, broke them into teams, which competed for and received points and badges. There was a huge uptake in results.
  </p>

  <p>
    <b>Caveats:</b> As put so well by Nevogt in the Huffington Post, gamification isn’t a panacea. It can’t cure an office with deeper organizational problems. The best-designed gamification — with all the player modeling, iteration and prototyping bells and whistles available — will be of limited value If the company’s CEO is collecting kickbacks from the mob. Also, games must be designed so that people are motivated to improve their job performance as opposed to winning money or awards. Finally, as in the game of marbles, the game design has to leave us wide-eyed with delight and anticipation.

  </p>


 


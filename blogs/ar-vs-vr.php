                    <p style="color: black;">Another AR project involved bringing a historical figure of Connecticut (the home-base of ARSOME) to life — Mark Twain. By pointing your phone at the sculpture of him in Hartford, Connecticut with the free downloadable app, Mark Twain comes to life right in front of users and tells them a little bit about himself. These experiences are not only fun, but memorable, especially for a younger audience.</p>

                    <p style="color: black;">
                     Understanding what augmented and virtual reality are is an important step in staying up to date in the technological world and being aware of your own options in your life and/or business. These technologies are here to stay and will only become more advanced and widely used into the future.

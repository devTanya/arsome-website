<h3>
   If you work in the digital space of Augmented and Virtual Reality Technology, one of the questions you may get time and time again is “what exactly do you even do?” But really, the question should be “what don’t you do?”
</h3>

<br>
                       <img src="../images/blog/vr.jpg" alt="Image" class="rounded"><br><center><small style="text-align: center;">Photo by <a href="https://unsplash.com/@jeshoots?utm_source=medium&utm_medium=referral" style="color: black; text-decoration: underline" target="_blank">JESHOOTS.COM </a> on <a href="https://unsplash.com/?utm_source=medium&utm_medium=referral" style="color: black; text-decoration: underline" target="_blank">Unsplash</a></small></center><br>

<br>
                     <p style="color: black;"> The answer to this question starts with some pretty complicated definitions from Merriam Webster of Virtual Reality and Augmented Reality. Merriam Webster defines virtual reality as: “an artificial environment which is experienced through sensory stimuli (such as sights and sounds) provided by a computer and in which one’s actions partially determine what happens in the environment”. It defines augmented reality as: “An enhanced version of reality created by the use of technology to overlay digital information on an image of something being viewed through a device (such as a smartphone camera”. These are two definitions that can be found by just the minimum amount of googling, but it’s not satisfying to most viewers who aren’t already “tecchies” of some sort.</p>


                     <p style="color: black;">Augmented reality is an addition to one’s already existing environment using technology and virtual reality is the complete creation of a new environment for someone to participate in using technology. The technology being used is one of the biggest differences between these two. For augmented reality, one might use a cell phone, and even AR glasses. For virtual reality, it’s almost always VR goggles with a handset, though sometimes the creativity is pushed to the next level and the handset or environment you use the VR tech with is notably different or close to your reality enough for you to properly interact with the VR scene.

</p>

<p style="color: black;">Both Virtual Reality and Augmented Reality have obvious pros and cons. For virtual reality, the biggest pros are the full immersion, escape from reality, and full creative freedom from anyone working with the technology. The larger cons are that virtual reality blocks out the outside world so you’re not aware of your surroundings, and the escape from reality may become addictive to some. The most impactful aspects of augmented reality are that you only need your smartphone, it can help clarify the world around you by adding information into your space, and it allows for projector mapping where you can overlay a projected image over a physical object. Aspects of augmented reality that are not so pleasing are: it can distract from the real world (which could be a pro in certain situations), and it is not fully immersive so your attention can still be divided.</p>

                     <blockquote class="h3" style="color: black;">Virtual reality and augmented reality are both currently being used to better lives.</blockquote>
                     <p style="color: black;">Virtual reality has been most frequently used for gaming, but it’s also being used by doctors to better understand medical procedures, and patients to manage pain through distraction and physical therapy exercises. It also is being used for training in cases where it may be too dangerous to be on the field.</p>


<br>
                     <img src="../images/blog/ar.jpg" alt="Image" class="rounded"><br><center><small style="text-align: center;">Photo by <a href="https://unsplash.com/@patrick_schneider?utm_source=medium&utm_medium=referral" style="color: black; text-decoration: underline" target="_blank">Patrick Schneider</a> on <a href="https://unsplash.com/?utm_source=medium&utm_medium=referral" style="color: black; text-decoration: underline" target="_blank">Unsplash</a></small></center><br><br>


                     <p style="color: black;">Some direct examples of virtual reality are ARSOME Technology’s learning simulations. One may want a training program for mining, for example. Through this simulation, the trainees can put on goggles and simulate job activities to decrease injury and better prepare for the actual thing. Trainees learn the proper attire and equipment to safely work on the job-site, check heavy machinery for malfunctions, and problem solve as a team with multiplayer capability. Immersive training like this is about as close as one can get to real world experience without half the amount of danger and even fear from the trainee.

</p>


                    <p style="color: black;">Another example of ARSOME Technology’s capability of virtual reality and learning simulations is the VR Apollo 11 story. It’s geared for space enthusiasts and simulates the experience of the astronauts who launched in 1969. The simulator not only lets one live in the actual environment, but it also has questions and motions for one to make so that they feel truly engaged and immersed in that world. Teaching history moves into the world of “experiencing” history just like that.</p>


                    <p style="color: black;">Augmented reality is something that has been catching on very slowly for a couple of years. Something most people remember is “Google Glass” — what was supposed to be a revolutionary idea for augmented reality glasses. Unfortunately, it didn’t take off as expected, however many other uses for AR have been identified. It is currently being used by doctors to visualize surgeries before going into them, as a device that allows medical professionals to see veins before pricking them, and in educational material to make it more engaging no matter one’s age. AR has also been used for projection mapping to project images of any sort onto physical objects to make them more dynamic and interesting to look at.</p>


                    <p style="color: black;">Some direct examples of uses of augmented reality are ARSOME Technology’s AR projects. The Tolerance Tykes book series is a great example of using AR to enhance storytelling and important messages within the book. On almost every other page, the reader can point their phone at the character in the bottom right corner and have that character pop up on their screen and begin speaking to the reader. The character works as an introduction to their own story leaving readers feeling more fulfilled and like a conversation was happening before they read the book and discuss the discussion questions.</p>
